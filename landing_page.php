<script>
    function closeMessage(messageId) {
        document.getElementById(messageId).style.display = 'none';
    }

    window.onload = function() {
        var messages = ["Hi! How are you?😊", "Feel free to report your complaint", ]; // Array of messages to display
        var index = 0;
        var speed = 40; // Adjust typing speed (milliseconds per character)

        function typeWriter() {
            var currentMessage = messages[index];
            document.getElementById('popup' + (index + 1)).style.display = 'block';
            typeMessage(0, currentMessage, 'message' + (index + 1));
            index++;
            if (index < messages.length) {
                setTimeout(typeWriter, currentMessage.length * speed + 3000); // Delay between messages
            }
        }

        function typeMessage(i, message, messageId) {
            if (i < message.length) {
                document.getElementById(messageId).innerHTML += message.charAt(i);
                i++;
                setTimeout(function() {
                    typeMessage(i, message, messageId);
                }, speed);
            }
        }

        setTimeout(typeWriter, 2000); // Delay before typing starts (2000 milliseconds = 2 seconds)
    };
</script>
